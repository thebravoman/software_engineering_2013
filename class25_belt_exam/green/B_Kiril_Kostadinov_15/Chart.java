
public class Chart {
	int a;
	int b;
	int c;
	
	Chart(int v1, int v2, int v3) {
		setA(v1);
		setB(v2);
		setC(v3);
	}
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}
	public int getB() {
		return b;
	}
	public void setB(int b) {
		this.b = b;
	}
	public int getC() {
		return c;
	}
	public void setC(int c) {
		this.c = c;
	}
}
