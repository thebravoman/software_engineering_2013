
public class PieAndBar {

        protected int val1;
        protected int val2;
        protected int val3;
        
        PieAndBar(int value1, int value2, int value3){
                this.val1 = value1;
                this.val2 = value2;
                this.val3 = value3;
        }
        
        public int getVal1() {
                return val1;
        }
        public void setVal1(int value1) {
                this.val1 = value1;
        }
        public int getVal2() {
                return val2;
        }
        public void setVal2(int value2) {
                this.val2 = value2;
        }
        public int getVal3() {
                return val3;
        }
        public void setVal3(int value3) {
                this.val3 = value3;
        }
        
}
