
public class Teacher {
		private String teacherName;
		private String teacherSpec;
		private int age;
		private int teacherClass;
		
		public String getTeacherName() {
			return teacherName;
		}
		public void setTeacherName(String teacherName) {
			this.teacherName = teacherName;
		}
		public String getTeacherSpec() {
			return teacherSpec;
		}
		public void setTeacherSpec(String teacherSpec) {
			this.teacherSpec = teacherSpec;
		}
		public int getAge() {
			return age;
		}
		public void setAge(int age) {
			this.age = age;
		}
		public int getTeacherClass() {
			return teacherClass;
		}
		public void setTeacherClass(int teacherClass) {
			this.teacherClass = teacherClass;
		}
	}
