
public class School {
	private String schoolName;
	private String schoolAddres;
	private String schoolTelephonNumber;
    
    
    
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getSchoolAddres() {
		return schoolAddres;
	}
	public void setSchoolAddres(String schoolAddres) {
		this.schoolAddres = schoolAddres;
	}
	public String getSchoolTelephonNumber() {
		return schoolTelephonNumber;
	}
	public void setSchoolTelephonNumber(String schoolTelephonNumber) {
		this.schoolTelephonNumber = schoolTelephonNumber;
	}
	
}
