
public class Uchenik {
	public String pulnoIme;
	public int klas;
	public int vipusk;
	public String adres;
	
	public String getPulnoIme(){
		return pulnoIme;
	}
	public void setPulnoIme(String pulnoIme){
		this.pulnoIme = pulnoIme;
	}
	
	public int getKlas(){
		return klas;
	}
	public void setKlas(int klas){
		this.klas = klas;
	}
	
	public int getVipusk(){
		return vipusk;
	}
	public void setVipusk(int vipusk){
		this.vipusk = vipusk;
	}
	
	public String getAdres(){
		return adres;
	}
	public void setAdres(String adres){
		this.adres = adres;
	}
}
